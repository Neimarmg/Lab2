package M;

public class Medicamentos {
    int codMedicamento, codTipoMedicamento;
    String descMedicamento;

    public int getCodMedicamento() {
        return codMedicamento;
    }

    public void setCodMedicamento(int codMedicamento) {
        this.codMedicamento = codMedicamento;
    }

    public int getCodTipoMedicamento() {
        return codTipoMedicamento;
    }

    public void setCodTipoMedicamento(int codTipoMedicamento) {
        this.codTipoMedicamento = codTipoMedicamento;
    }

    public String getDescMedicamento() {
        return descMedicamento;
    }

    public void setDescMedicamento(String descMedicamento) {
        this.descMedicamento = descMedicamento;
    }
    
    
}
