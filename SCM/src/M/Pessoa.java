package M;

public class Pessoa {
    int codPessoam, codTipo, codProfissao, cpf ;
    String nome;

    public int getCodPessoam() {
        return codPessoam;
    }

    public void setCodPessoam(int codPessoam) {
        this.codPessoam = codPessoam;
    }

    public int getCodTipo() {
        return codTipo;
    }

    public void setCodTipo(int codTipo) {
        this.codTipo = codTipo;
    }

    public int getCodProfissao() {
        return codProfissao;
    }

    public void setCodProfissao(int codProfissao) {
        this.codProfissao = codProfissao;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
